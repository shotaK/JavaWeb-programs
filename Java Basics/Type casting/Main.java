package com.javaweb;

public class Main {

    public static void main(String[] args) {

        /*--------------Automatic conversion----------------*/
        int i = 15;
        long l = i;
        System.out.println(l);

        /*--------------convert double to int with type casting-----------------*/
        double d = 3.1372;
        int i2 = (int) d;
        System.out.println(i2); // result: 3

    }
}
